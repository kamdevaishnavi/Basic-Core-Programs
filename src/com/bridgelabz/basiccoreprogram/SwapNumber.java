package com.bridgelabz.basicprogram;

public class SwapNumber {
    public static void main(String[] args) {
        int num1 = 50;
        int num2 = 100;
        int temp;
        temp = num1;
        num1 = num2;
        num2 = temp;
        System.out.println("num1 = " +num1);
        System.out.println("num2 = " +num2);
    }
}

